package section9IntroductionToOOP;

public class ClassObj {

    //CLASS
    /*
    A class is a blueprint or template that defines the structure and behavior (fields and methods) of objects.
    It doesn't hold actual data but describes what an object will have and do.

    public class Car {
    String color;
    String model;

    void drive() {
        System.out.println("The car is driving.");
    }
}
     */

    //OBJECT
    /*
    An object is an instance of a class.
    It holds real data and can use the behaviors defined in the class.
    You create objects using the new keyword.

        Car myCar = new Car();  // Object creation
        myCar.color = "Red";
        myCar.model = "Toyota";
        myCar.drive();
     */

    //In short:

    //Class = blueprint
    //Object = actual item created from the blueprint

}